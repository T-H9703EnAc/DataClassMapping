package app.com.beans;

public class DataA {
	private int id;
	private String name;
	private int age;
	private int[] datas;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int[] getDatas() {
		return datas;
	}
	public void setDatas(int[] datas) {
		this.datas = datas;
	}
	
	
}
